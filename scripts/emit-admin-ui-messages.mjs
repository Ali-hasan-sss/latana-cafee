function deepMap(obj, fn) {
  if (obj === null || typeof obj !== "object") return fn(obj);
  if (Array.isArray(obj)) return obj.map((x) => deepMap(x, fn));
  const out = {};
  for (const [k, v] of Object.entries(obj)) {
    out[k] = deepMap(v, fn);
  }
  return out;
}

